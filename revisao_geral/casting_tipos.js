
// convertendo os tipos de váriaveis


/* utilizamos então...


para inteiro:

parseInt(var)


para numero float:

parseFloat(var)



para string:

.toString()


*/

var variavel = prompt('digite algo')
var variavel1 = prompt('digite algo denovo')

variavel = parseInt(variavel)
variavel1 = parseInt(variavel1)

variavel = variavel.toString()
variavel1 = variavel1.toString()

document.write(variavel + variavel1)