//uso do Math
/*

função Math.ceil -> arredondamento para o numero subsequente

função Math.floor -> arredondamento para o numero anterior

função Math.round -> arredondamento com base na parte decimal

função Math.random -> retorna um número aleatório entre 0 e 1, que é decimal

*/

var x = Math.ceil(11.01)
document.write(x + '<br/>')

var y = Math.floor(11.31)
document.write(y+ '<br/>')

var z = Math.round(1.6)
document.write(z + '<br/>')

var a = Math.random()
document.write(a + '<br/>')




